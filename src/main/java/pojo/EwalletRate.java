package pojo;

public class EwalletRate {
	
	 private String ewallet_type;
	 private String ewallet_charge_type;
	 private double ewallet_fee;
	 
	 
	public String getEwallet_type() {
		return ewallet_type;
	}
	public void setEwallet_type(String ewallet_type) {
		this.ewallet_type = ewallet_type;
	}
	public String getEwallet_charge_type() {
		return ewallet_charge_type;
	}
	public void setEwallet_charge_type(String ewallet_charge_type) {
		this.ewallet_charge_type = ewallet_charge_type;
	}
	public double getEwallet_fee() {
		return ewallet_fee;
	}
	public void setEwallet_fee(double ewallet_fee) {
		this.ewallet_fee = ewallet_fee;
	}

}
